
package operacionmatematica;


public class OperacionMatematica {

    
    public static void main(String[] args) {
       int n1 =20, n2 = 30;
       int a=n1+n2;
       int b=n2-n1;
       int c=n1*n2;
       int d=n2/n1;
       
       
       System.out.println(" n1+n2="+a);
       System.out.println(" n2-n1="+b);
       System.out.println(" n1*n2="+c);
       System.out.println(" n2/n1="+d);
    }
    
}
